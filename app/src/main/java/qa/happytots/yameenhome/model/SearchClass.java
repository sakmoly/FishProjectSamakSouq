package qa.happytots.yameenhome.model;

public class SearchClass {

    int id;
    String search;

    public SearchClass() {
    }

    public SearchClass(String search) {
        this.search = search;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSearch() {
        return search;
    }

    public void setSearch(String search) {
        this.search = search;
    }


}
